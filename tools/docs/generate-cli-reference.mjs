import { spawnSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(fileURLToPath(new URL("../..", import.meta.url)));

function parseArgs(argv) {
  const options = {
    output: "docs/cli-reference.md",
    input: "",
    check: false,
    stdout: false,
  };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    switch (arg) {
      case "--output":
        options.output = requireValue(argv, ++index, arg);
        break;
      case "--input":
        options.input = requireValue(argv, ++index, arg);
        break;
      case "--check":
        options.check = true;
        break;
      case "--stdout":
        options.stdout = true;
        break;
      default:
        throw new Error(`unknown argument: ${arg}`);
    }
  }
  return options;
}

function requireValue(argv, index, flag) {
  const value = argv[index];
  if (!value || value.startsWith("--")) {
    throw new Error(`${flag} requires a value`);
  }
  return value;
}

function loadCommandCatalog(options, includeAll = true) {
  if (options.input) {
    return JSON.parse(readFileSync(path.resolve(rootDir, options.input), "utf8"));
  }
  const args = ["run", "./cmd/agent-testbench", "commands"];
  if (includeAll) {
    args.push("--all");
  }
  args.push("--json");
  const result = spawnSync("go", args, {
    cwd: rootDir,
    encoding: "utf8",
    stdio: "pipe",
  });
  if (result.status !== 0) {
    throw new Error(result.stderr || result.stdout || "failed to read command catalog");
  }
  return JSON.parse(result.stdout);
}

function renderCLIReference(report, defaultReport) {
  const commands = Array.isArray(report.commands) ? report.commands : [];
  const defaultCommands = Array.isArray(defaultReport?.commands)
    ? defaultReport.commands
    : commands.filter((command) => command.reason);
  const areas = groupBy(commands, (command) => command.area || "(none)");
  const lines = [
    "# AgentTestBench CLI Reference",
    "",
    "Generated from `agent-testbench commands --all --json`. Do not edit this file by hand; run `npm run docs:cli-reference` after changing CLI usage or replacement guidance.",
    "Internal maintenance and diagnostics commands are intentionally excluded; use `agent-testbench commands --all --internal --json` when debugging the CLI/runtime itself.",
    "",
    `Total commands: \`${commands.length}\``,
    `Default commands: \`${defaultCommands.length}\``,
    "",
    "## Default Commands",
    "",
    "| Command | Store | Usage |",
    "| --- | --- | --- |",
    ...defaultCommands.map((command) => `| \`${escapeMarkdown(command.command)}\` | ${command.storeAware ? "yes" : "no"} | \`${escapeMarkdown(command.usage)}\` |`),
    "",
    "## Command Areas",
    "",
    "| Area | Commands | Store-aware | Replacements |",
    "| --- | ---: | ---: | ---: |",
  ];

  for (const area of Object.keys(areas).sort()) {
    const areaCommands = areas[area];
    lines.push(`| \`${escapeMarkdown(area)}\` | ${areaCommands.length} | ${areaCommands.filter((command) => command.storeAware).length} | ${areaCommands.filter((command) => command.replacement).length} |`);
  }

  lines.push("");
  for (const area of Object.keys(areas).sort()) {
    lines.push(`### \`${escapeMarkdown(area)}\``, "");
    lines.push("| Command | Store | Replacement | Usage |");
    lines.push("| --- | --- | --- | --- |");
    for (const command of areas[area]) {
      lines.push([
        `\`${escapeMarkdown(command.command)}\``,
        command.storeAware ? "yes" : "no",
        command.replacement ? `\`${escapeMarkdown(command.replacement)}\`` : "",
        `\`${escapeMarkdown(command.usage)}\``,
      ].join(" | ").replace(/^/, "| ").replace(/$/, " |"));
    }
    lines.push("");
  }
  return `${lines.join("\n").trimEnd()}\n`;
}

function groupBy(items, keyFn) {
  const groups = {};
  for (const item of items) {
    const key = keyFn(item);
    groups[key] ??= [];
    groups[key].push(item);
  }
  return groups;
}

function escapeMarkdown(value) {
  return String(value || "")
    .replace(/\\/g, "\\\\")
    .replace(/\|/g, "\\|")
    .replace(/`/g, "\\`")
    .replace(/\r?\n/g, " ");
}

function main() {
  const options = parseArgs(process.argv.slice(2));
  const outputPath = path.resolve(rootDir, options.output);
  const allReport = loadCommandCatalog(options, true);
  const defaultReport = options.input ? undefined : loadCommandCatalog(options, false);
  const rendered = renderCLIReference(allReport, defaultReport);
  if (options.stdout) {
    process.stdout.write(rendered);
    return;
  }
  if (options.check) {
    const current = readFileSync(outputPath, "utf8");
    if (current !== rendered) {
      throw new Error(`${path.relative(rootDir, outputPath)} is out of date; run npm run docs:cli-reference`);
    }
    console.log(`${path.relative(rootDir, outputPath)} is up to date`);
    return;
  }
  writeFileSync(outputPath, rendered);
  console.log(`wrote ${path.relative(rootDir, outputPath)}`);
}

try {
  main();
} catch (error) {
  console.error(error.message);
  process.exit(1);
}
